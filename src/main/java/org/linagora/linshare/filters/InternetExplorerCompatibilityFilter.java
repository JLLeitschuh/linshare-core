package org.linagora.linshare.filters;

import java.io.IOException;
import java.util.regex.Matcher;
import java.util.regex.Pattern;

import javax.servlet.FilterChain;
import javax.servlet.ServletException;
import javax.servlet.http.HttpServletRequest;
import javax.servlet.http.HttpServletResponse;

import org.springframework.web.filter.OncePerRequestFilter;

/**
 * This filter is designed to fix Internet explorer 8 an d 9 compatibility with
 * application/json content type for fineuploader upload URL .
 * 
 * @author fmartin
 * 
 */
public class InternetExplorerCompatibilityFilter extends OncePerRequestFilter {

	@Override
	protected void doFilterInternal(HttpServletRequest request, HttpServletResponse response, FilterChain filterChain)
			throws ServletException, IOException {

		String method = request.getMethod();
		if (method.equals("POST")) {

			if (request.getHeader("User-Agent") != null) {
				String userAgent = request.getHeader("User-Agent");
				logger.debug("userAgent : " + userAgent);

				Pattern pattern = Pattern.compile(".*MSIE [8,9]\\.0.*");
				Matcher matcher = pattern.matcher(userAgent);
				if (matcher.matches()) {
					response.setHeader("Content-Type", "text/html");
				}
			}
		}
		filterChain.doFilter(request, response);
	}

}
